    <h1 align="center">International Students/Admission Criteria</h1>
     <button class="collapsible">Filling the application form</button>
<div class="content">
  <p>1.The applicant has to fill the online application form for the international applicants available at www.acetamritsar.ac.in by filling the registration form and login into the online admission portal.</p>
<p>2.The applicant has to ensure that the application form is duly filled and the documentary evidence of qualifying examination supporting the admission (if any) is also uploaded in the online application portal. </p>
<p>3.If the content in the marks sheet/ transcript is not in English, a certified English translated version must also be uploaded in addition to the native language certificate.</p>
    <p>4.In case the results have not been declared, “result awaited” should be filled in the Result Status column.</p>
<p>5. The applicant has to visit the online application portal time to time for all the latest updates related to the admission as all the further communication will be through the portal only. The College will not intimate personally to any applicant and through any other mode.</p>
</div>


<button class="collapsible">Eligibility verification and issuing of  offer letter</button>
<div class="content">
  <p>After receiving the online application and fulfilling of due formalities by due date as required, the College may issue “Offer Letter (OL)” for provisional admission or “Conditional Offer Letter” as the case may be.</p>
</div>
<button class="collapsible">Accepting term of accepting of offer letter and updating fee status</button>
<div class="content">
  <p>1.The Applicant has to agree to the terms of Acceptance of Offer Letter available in the portal by filling the details.</p>
<p>2.The student has to ensure that he/she has read and understood all the guidelines, policies and instructions as mentioned in the International Booklet 2018 and the College website www.acetamritsar.ac.in before accepting the terms of Acceptance of Offer Letter. </p>
<p>3.The student has to fill the “Fee remittance details” before submitting the AOL.</p>
</div>
<button class="collapsible">Fee payment and compliance of the confiction</button>
<div class="content">
  <p>The applicant shall submit the fees and also fulfill all other condition(s) mentioned in the Offer Letter and acceptance Letter within stipulated time period.</p>
</div>
    
    <button class="collapsible">Issue of Acceptance Letter</button>
<div class="content">
  <p>1.After receiving the Acceptance of Offer Letter and verifying the fulfillment of all the conditions as mentioned in the Offer Letter and confirmation of the payment transfer details updated by the applicant in the online application portal, the College will issue the Acceptance Letter. The applicant can download his Acceptance Letter available in his online application portal or will be sent through email.</p>
<p>2.The Acceptance Letter issued by the College can be used for applying the Indian Student visa. Offer Letter cannot be used to apply for the Indian Student Visa..</p>
</div>
    
    <button class="collapsible">Visa Application</button>
<div class="content">
  <p>The applicant should apply for the student visa at the nearest Indian High Commission/ Indian Embassy. The application for Visa should be supported by all the relevant documents.</p>
</div>
    
    <button class="collapsible">Reporting at the College campus</button>
<div class="content">
  <p>1.The student shall report at the College before the last date of reporting as mentioned in the Offer Letter or as available on the College website www.acetamritsar.ac.in ( reporting of program under the Dean Admissions).</p>
<p>2.In case of any delay in reporting because of any reason including but not limited to issue of visa, the student shall not be allowed to join the College, unless otherwise decided by the College.</p>
<p>3.In case, student reports at College after the last date of admission, he/she has to take special approval for it and shall have to give an undertaking that he/she will not claim any benefit related to loss in attendance, marks, or CA. For more information on the late joining (refer to head reporting of International Booklet 2017)</p>
</div>
    
    <button class="collapsible">FRRO and Medical Certificate</button>
<div class="content">
  <p>1.The FRRO is mandatory for all Foreign nationals (except the students from Nepal and Bhutan) studying in India. The FRRO has to be completed within 14 days of arrival in India and the student has to provide the College with a copy of the FRRO.</p>
<p>2.The student has to undergo medical examination in his/her native country for general fitness, HIV, and yellow fever (in case of African countries) and has to provide the copy of the same at the time of reporting. In case of any non-compliance, the admission of the student can be cancelled or any other action may be taken as deemed appropriate by the College and concerned Government agency. (For complete information, refer the head general information related to foreigner regional registration)</p>
</div>
    
    
    

