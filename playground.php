    <h1 align="center">PlayGround/Sports</h1>
    <p>All work and no play make Jack a dull boy so goes a well-known saying. Sports are essential to revitalizing the energies of students who spend most of their times in classrooms and labs. In such a sedentary lifestyle, sports lend a zest and spirit leading to the refreshment of mind and body. The sports department of ACET stresses on physical activities like running, jumping and stretching that is instrumental in the constructive expenditure of energy. Students are encouraged to play sports like Cricket, Badminton, Basketball, Chess, Swimming, Football, Athletics and Kabaddi. Sports department at ACET provides top facilities for sports to all students. The total playground area in the College Campus is a magnetic 2.5 acres. There are in total three playgrounds in the college. A college has tied up with a local stadium at Manawala (Jagir Singh Sandhu Memorial Stadium) for conducting various Sports activities and tournaments. Our College organizes regular sports events within the campus itself. Many of our students have participated in inter college competitions and have won several prizes in various categories.
