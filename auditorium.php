        <h1 align="center">Auditorium</h1>
    <p>Since humanity started gathering to tell stories and represent scenes from everyday life in front of an audience, the need for a space to perform such activities began to increase. Theater design developed from the open-air amphitheaters of the Greeks and Romans to the incredible array of forms we see today. Though some forms work better for particular types of performance, there is no ideal shape or size of a theater. The choice of the best form and scale depends on the functional purpose (movies, lectures, stage performances, musical presentations), the size of the staging required and the number of the audience to be accommodated.</p><br>
    <h3 align="center">All these facilities are of prime importance, but before considering any venue make sure that:</h3>
        
    <p>1.Stalls, orchestra or arena: the lower flat area, usually below or at the same level as the stage.</p>
<p>2.Balconies or galleries: one or more raised seating platforms towards the rear of the auditorium. In larger theatres, multiple levels are stacked vertically above or behind the stalls. The first level is usually called the dress circle or grand circle. The highest platform, or upper circle is sometimes known as the gods, especially in large opera houses, where the seats can be very high and a long distance from the stage.</p>
<p>3.Boxes: generally placed immediately to the front, side and above the level of the stage. They are often separate rooms with an open viewing area which generally seat only a handful of people. These seats are generally considered the most prestigious of the house. A state box or royal box is sometimes provided for dignitaries.</p>
<p>4.Seating arrangement: Seating arrangements in an auditorium seating layout (or assembly space) will either be identified as “multiple-aisle” or “continental.”[1] These terms are commonly found in design standards manuals, building codes, and similar architectural reference documents. Each size is unique, with specific guidelines governing row size, row spacing, and exit ways. Basically, a multiple-aisle arrangement will have a maximum of 14–16 chairs per row with access to an aisle-way at both ends. In a continental arrangement, all seats are located in a central section. Here the maximum quantity of chairs per row can greatly exceed the limits established in a multiple-aisle arrangement. In order to compensate for the greater length of rows allowed, building codes will require wider row spacing, wider aisles, and strategically located exit doors. Although it would seem like more space is called for, a continental seating plan is often not any less efficient than a multiple-aisle arrangement. In fact, if it is carefully planned, a continental arrangement can frequently accommodate more seating within the same space.</p>
