        <h1 align="center">Anti Ragging Comm.</h1>
    <p>Ragging is a practice in educational institutions that involves existing students baiting or bullying new students. In the name of introduction or initiation, millions of students are brutally abused in many educational institutions. Historically originated, probably, to generate fellow-feeling or camaraderie and the ability to work in a team, it has metamorphosed into something else. It often takes a malignant form wherein the newcomers may be subjected to psychological or physical torture.</p>

<p>Under the pretext of ‘welcoming’ new students to the college, ragging is a notorious practice that gives senior students an excuse to harass their junior counterparts and make them easy targets to satiate their own perverse sadistic pleasures.</p>
    
        
        <p>What Is Ragging</p>
        <p>( As defined by the Honourable Supreme Court of India )</p>

<p>Any disorderly conduct whether by words spoken or written, or by an act which has the effect of teasing, treating or handling with rudeness any other student, indulging in rowdy or undisciplined activities which causes or is likely to cause annoyance, hardship or psychological or physical harm or to raise fear or apprehension thereof in a fresher or a junior student and which has the effect of causing or generating a sense of shame or embarrassment so as to adversely affect the physique or psyche of a fresher or a junior student.</p>
        <p>How are Freshers / Juniors Protected?</p>
<p>Today, students can file a complaint against the person who has hurt him or her emotionally, physically or mentally with the Anti-Ragging Cell that is set up in the college. One of the pre-emptive measures in our institution to curb ragging includes submission of undertaking by all students and their parents, at the time of admission, taking oath not to indulge in ragging.</p>
<p>
    Punishment For Ragging: </p>
<p>Ragging within or outside the educational institution is prohibited. Whoever directly or indirectly commits, participates in, abets or instigates ragging inside or outside the educational institution shall be suspended, expelled or rusticated from the institution and shall also be liable to fine. The punishment includes rigorous imprisonment, cancellation of admission, suspension from attending classes, withholding/ withdrawing fellowships/ scholarships and other financial benefits, withholding or cancelling the results.</p>

 <p>Depending upon the nature and gravity of the offence as established by the Anti-Ragging Committee of the Institution, the possible punishments for those found guilty of ragging shall be any one or any combination of the following:</p>

        <p>1.Suspension from attending classes and academic privileges</p>
        <p>2.Withholding / withdrawing scholarship / fellowship and other benefits</p>
<p>3.Forfeiting Campus Placement opportunities / recommendations</p>
<p>4.Debarring from appearing in any test/examination or other evaluation process</p>
<p>5.Withholding of results</p>
<p>6.Debarring from representing the institution in any regional, national or international meet, tournament, youth festival etc.</p>
<p>7.Suspension/ expulsion from the hostel</p>
<p>8.Cancellation of Admission</p>
<p>9.Rustication from the institution for a period, ranging from 1 to 3 years</p>
        <p>10.Expulsion from the institution and consequent debarring from admission to any other institution for a specific period</p>
        <p>11.Fine of Rupees 25,000/-</p>
        <p>12.Collective punishment: When the persons committing or abetting the crime of ragging are not identified, the institution shall resort to collective punishment as a deterrent to ensure community pressure on the potential raggers.</p>
<p>13.In addition, in case of every single incident of ragging, a First Information Report (FIR) will be filed with the local police authorities. The possible punishment includes rigorous imprisonment (in compliance with the order of Supreme Court of India)</p>

<p>14.In the case of any ragging incident, the students may immediately call and bring it to the notice of the members of the Anti-Ragging committee for necessary help, guidance and actions.</p>
